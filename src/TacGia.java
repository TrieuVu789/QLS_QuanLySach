public class TacGia {
    private String ten;
    private int ngaySinh;

    public TacGia(String ten, int ngaySinh) {
        this.ten = ten;
        this.ngaySinh = ngaySinh;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getNgaySinh() {
        return ngaySinh;
    }

    public void setNgaySinh(int ngaySinh) {
        this.ngaySinh = ngaySinh;
    }
}
